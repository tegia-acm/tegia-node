person_t::person_t():contact_t("person")
{
	
};


///////////////////////////////////////////////////////////////////////////////////////////
/*

*/
///////////////////////////////////////////////////////////////////////////////////////////


std::string person_t::value() const
{
	return this->surname + " " + this->name + " " + this->patronymic + " " + this->t_birth;
};


///////////////////////////////////////////////////////////////////////////////////////////
/*

*/
///////////////////////////////////////////////////////////////////////////////////////////


void person_t::parse(nlohmann::json * data)
{
	if(data->contains("surname") == true)
	{
		this->surname = (*data)["surname"].get<std::string>();
	}

	if(data->contains("name") == true)
	{
		this->name = (*data)["name"].get<std::string>();
	}

	if(data->contains("patronymic") == true)
	{
		this->patronymic = (*data)["patronymic"].get<std::string>();
	}

	if(data->contains("t_birth") == true)
	{
		this->t_birth = (*data)["t_birth"].get<std::string>();
	}
};


